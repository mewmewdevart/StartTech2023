/* EXISTEM MAIS RETORNOS
    undefined: O valor undefined é retornado quando uma variável foi declarada, 
mas ainda não recebeu um valor. Também é o valor de retorno padrão para funções que não têm uma
declaração explícita de retorno.

    NaN: A sigla "NaN" significa "Not a Number" (Não é um número). É um valor especial que representa
um resultado inválido em operações aritméticas ou matemáticas. Por exemplo, quando você tenta realizar
uma operação de divisão por zero ou quando uma operação matemática é aplicada a um valor que não é um
número válido, o resultado será NaN.

    null: O valor null é uma palavra-chave que representa a ausência intencional de qualquer objeto
ou valor. É usado para indicar a falta de valor ou a não existência de um objeto.

    string: Uma string é uma sequência de caracteres. É usada para representar texto no JavaScript.
As strings são delimitadas por aspas simples ('') ou aspas duplas ("").
    
    number: O tipo de dado number representa números numéricos. O JavaScript suporta tanto números
inteiros quanto números de ponto flutuante. É importante mencionar que o JavaScript não diferencia
entre números inteiros e números de ponto flutuante; ambos são tratados como do tipo number.

    boolean: O tipo de dado boolean representa um valor lógico que pode ser true (verdadeiro) ou false (falso).
É usado para representar o resultado de expressões condicionais e operações lógicas.
*/