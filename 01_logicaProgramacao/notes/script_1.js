// Exemplo de Declaração, Atribuição e Incremento de Variáveis em JavaScript
// Essa função exibirá no console
console.log("Olá, tudo bem?");
// Essa função exibirá uma mensagem de aviso no console
console.warn("Toma cuidado!");
// Essa função exibirá uma mensagem de erro no console
console.error("Deu ruim...:(");

// Declaração e inicialização de variáveis:
var student_grade = 9.5;
console.log(student_grade);

// Atribuição de valores às variáveis do tipo int e float:
var student_grade_inteiro;
var student_grade_float;

student_grade_inteiro = 10;
student_grade_float = 10.5;

// Diferentes formas de incrementar/decrementar um valor a uma variavel
var numberLucas = 5;

numberLucas = numberLucas + 1;
numberLucas += 1;
numberLucas -= 1;
numberLucas++;
numberLucas--;

// Incremento de variáveis:
var teste = 1;

teste++; // (incrementando o valor da variável teste em 1)
++teste; // (incrementando o valor da variável teste em 1 antes da exibição)

// Operações matematicas
console.log("Conta de multiplicação: ", 5 * 4);
console.log("Conta de divisão: ", 20 / 4);
console.log("Exponenciação: ", 5 ** 4);
console.log("Resto da divisão: ", 20 % 4);